'use strict';

/**
* @param {number} a
* @param {number} b
* @returns {number}
*/

//сложение
function addition(a, b) {
    return a + b;
}

//вычитание
function subtraction(a, b) {
    return a - b;
}

//деление
function division(a, b) {
    return a / b;
}

//умножение
function multiplication(a, b) {
    return a * b;
}